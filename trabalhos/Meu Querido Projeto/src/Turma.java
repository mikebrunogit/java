
public class Turma {

	String nomeTurma, numeroTurma, materiasTurma, professoresTurma, alunosTurma;

	public String getNomeTurma() {
		return nomeTurma;
	}

	public void setNomeTurma(String nomeTurma) {
		this.nomeTurma = nomeTurma;
	}

	public String getNumeroTurma() {
		return numeroTurma;
	}

	public void setNumeroTurma(String numeroTurma) {
		this.numeroTurma = numeroTurma;
	}

	public String getMateriasTurma() {
		return materiasTurma;
	}

	public void setMateriasTurma(String materiasTurma) {
		this.materiasTurma = materiasTurma;
	}

	public String getProfessoresTurma() {
		return professoresTurma;
	}

	public void setProfessoresTurma(String professoresTurma) {
		this.professoresTurma = professoresTurma;
	}

	public String getAlunosTurma() {
		return alunosTurma;
	}

	public void setAlunosTurma(String alunosTurma) {
		this.alunosTurma = alunosTurma;
	}
	
}
